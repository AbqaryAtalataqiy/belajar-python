print("Binary Operator")

number1 = int(input("Masukkan Angka Pertama: "))
number2 = int(input("Masukkan Angka Kdeua: "))

and_operator = number1 & number2
or_opetaror = number1 | number2
xor_operator = number1 ^ number2
not_operator = number1
not_operator = number2

print(f"{number1} & {number2} = {and_operator}")
print(f"{number1} | {number2} = {or_opetaror}")
print(f"{number1} ^ {number2} = {xor_operator}")
print(f"~{number1} = {not_operator}")
print(f"~{number2} = {not_operator}")
